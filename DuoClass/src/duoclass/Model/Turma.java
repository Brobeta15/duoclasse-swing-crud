
package duoclass.Model;

public class Turma {
    
    private int cd_turma;
    private String nome_turma;
    private int cd_professor;

    public int getCd_turma() {
        return cd_turma;
    }

    public String getNome_turma() {
        return nome_turma;
    }

    public int getCd_professor() {
        return cd_professor;
    }

    public void setCd_turma(int cd_turma) {
        this.cd_turma = cd_turma;
    }

    public void setNome_turma(String nome_turma) {
        this.nome_turma = nome_turma;
    }

    public void setCd_professor(int cd_professor) {
        this.cd_professor = cd_professor;
    }
    
    
}
