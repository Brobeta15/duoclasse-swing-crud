
package duoclass.Model;

public class Atividade {
    
    private int cd_atividade;
    private String nome_atividade;
    private String ds_atividade;
    private int cd_turma;

    public int getCd_atividade() {
        return cd_atividade;
    }

    public String getNome_atividade() {
        return nome_atividade;
    }

    public String getDs_atividade() {
        return ds_atividade;
    }

    public int getCd_turma() {
        return cd_turma;
    }

    public void setCd_atividade(int cd_atividade) {
        this.cd_atividade = cd_atividade;
    }

    public void setNome_atividade(String nome_atividade) {
        this.nome_atividade = nome_atividade;
    }

    public void setDs_atividade(String ds_atividade) {
        this.ds_atividade = ds_atividade;
    }

    public void setCd_turma(int cd_turma) {
        this.cd_turma = cd_turma;
    }
    
    
}
